package com.teachmeskills.lesson7HW.task2.student;

public class Student {
    public String name;
    public String surname;
    public String groupName;
    public int age;
    public String passNumber;

    public Student(String name, String surname, String groupName, int age, String passNumber){
        this.name = name;
        this.surname = surname;
        this.groupName = groupName;
        this.age = age;
        this.passNumber = passNumber;
    }
}
